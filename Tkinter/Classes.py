#Python 3
#Learning about the basics of classes
class calculator:
    def addition(x,y):
        ans = x+y
        print(ans)

    def subtraction(x,y):
        ans = x-y
        print(ans)

    def multiplication(x,y):
        ans = x*y
        print(ans)

    def division(x,y):
        ans = x/y
        print(ans)

calculator.multiplication(3,5)
calculator.addition(4,5)
